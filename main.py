#functions are defined with def, then a name followed by parentheses. Then a colon. What the function will do will go below, and will be indented.

def say_hi():
  print("Hello World!")

say_hi()

#while loops will repeat a task so long as the stated conditions are true. It is defined similarly to a for loop

steps = 6
while steps > 0:
  print("Took a step")
  steps -= 1

#unlike for loops, while loops can go infinite!